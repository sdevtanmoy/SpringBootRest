package com.java.professional;

public class Program108 {

	public static void main(String[] args) {
		String color = "Red";
		
		switch(color) {
		
		case "Red":
			System.out.println("Red");
		case "Blue":
			System.out.println("Blue");
		case "Teal":
			System.out.println("Teal");
			break;
		default:
			System.out.println("Default");
		}
	}
}
